import java.util.Arrays;
import java.util.Scanner;

public class charjava2 {
    public static void main(String[] args) {
        char[] ch = { 'h', 'e' };

        for (int i = 0; i < 2; i++) {
            System.out.println(ch[i]);
        }

        System.out.println(Arrays.toString(ch));
    }
}
